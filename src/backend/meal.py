class Meal:
    def __init__(self, name, ingredients, fans):
        pass

#getter

@property
def name(self):
    return self.name

@property
def ingredients(self):
    return self.ingredients

@property
def fans(self):
    return self.fans

#setter

@name.setter
def name(self, value):
    self._name = value

@ingredients.setter
def ingredients(self, value):
    self._ingredients = value

@fans.setter
def fans(self, value):
    self._fans = value

